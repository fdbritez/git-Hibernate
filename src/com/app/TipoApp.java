    private void txt_buscadorPorDescripcionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_buscadorPorDescripcionKeyPressed
         if(evt.getKeyCode()== KeyEvent.VK_ENTER){
       // this.retornoArticulo();
          this.cargarTabla();
            try{
                 if(this.txt_buscadorPorDescripcion.getText().isEmpty()){
                     JOptionPane.showMessageDialog(null,"Debes rellenar el campo buscar por Descripcion");
                 this.txt_buscadorPorDescripcion.requestFocus();
               }else{
                 this.limpiartabla();
             
                 this.buscadorPorDescripcionTabla();
                }
                
       }catch(Exception e){
           
       }
      }
    }//GEN-LAST:event_txt_buscadorPorDescripcionKeyPressed

