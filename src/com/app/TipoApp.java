      public void limpiartabla(){
      int a = model.getRowCount()-1;
         for(int i=a; i>=0; i--){
           model.removeRow(i);
      }
    }
     public void soloNumeros(KeyEvent evt){
      char car = evt.getKeyChar();
       if((car <'0' || car >'9'))
         evt.consume();
  }
