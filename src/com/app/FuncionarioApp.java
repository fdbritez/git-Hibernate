    public void buscadorPorNombreTabla(){
      try{
        st.beginTransaction();
      String porNombre =this.txt_buscadorPorNombre.getText();
    
      List<Funcionario> lista =(List<Funcionario>)st.createQuery("FROM Funcionario WHERE nombre_funcionario LIKE '%"+porNombre+"%'" ).list();
      
      for(Funcionario fun : lista){
            String nomApe = fun.getNombre_funcionario()+ " " + fun.getApellido_funcionario();
          
            model.addRow(new Object[]{
                fun.getId_funcionario(),
                nomApe,
                fun.getDireccion_funcionario(),
                fun.getTelefono_funcionario(),
                fun.getCargo_funcionario()
            });
        }
     }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Debes rellenar el campo buscar por nombre");  
     }
     this.obtenerFuncionaro();
    }  
    
    
