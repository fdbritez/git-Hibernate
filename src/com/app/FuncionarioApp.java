    public void guardar(){
        if(this.txt_nombre_funcionario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null ,"Falta nombre del Funcionario.");
            this.txt_nombre_funcionario.requestFocus();            
        }else{
            if(this.txt_apellido_funcionario.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Falta el apellido del Funcionario");
                this.txt_apellido_funcionario.requestFocus();
            }
            else{
                if(this.txt_direccion_fucionario.getText().isEmpty()){
                  //  JOptionPane.showMessageDialog(null ,"Falta la direccion del Funcionario");
                    JOptionPane.showMessageDialog(this, "Falta la direccion del Funcionario","Rellenar campo Direccion.",JOptionPane.QUESTION_MESSAGE);
                    this.txt_direccion_fucionario.requestFocus();
                }
                else{
                    if(this.txt_telefono_funcionario.getText().isEmpty()){
                        JOptionPane.showMessageDialog(this,"Falta el telefono del Funcionario","Rellene el campo de Telefono",JOptionPane.QUESTION_MESSAGE);
                        this.txt_telefono_funcionario.requestFocus();
                    }
                    else{
                        if(this.txt_jComboBox_funcionario.getSelectedIndex()== -1){
                            JOptionPane.showMessageDialog(this,"Falta cargo de Funcionario","Seleccione un cargo",JOptionPane.QUESTION_MESSAGE);
                            this.txt_jComboBox_funcionario.requestFocus();
                        }
                        else{
                            if(this.bt_guardar.getText().equals("Guardar")){
                                st.beginTransaction();
                                Funcionario func = new Funcionario();
                                func.setNombre_funcionario(this.txt_nombre_funcionario.getText());
                                func.setApellido_funcionario(this.txt_apellido_funcionario.getText());
                                func.setDireccion_funcionario(this.txt_direccion_fucionario.getText());
                                func.setTelefono_funcionario(this.txt_telefono_funcionario.getText());
                                func.setCargo_funcionario(String.valueOf(this.txt_jComboBox_funcionario.getSelectedItem()));
                                st.save(func);
                                st.getTransaction().commit();
                                this.arranque();
                                JOptionPane.showMessageDialog(null,"Registro guardado correctamente");
                                
                            }
                            else{
                                st.beginTransaction();
                                int selectedRow = this.jTable5.getSelectedRow();
                                int idFunc = Integer.parseInt(String.valueOf(model.getValueAt(selectedRow,0)));
                                Funcionario func = (Funcionario)st.load(Funcionario.class, idFunc);
                                func.setNombre_funcionario(this.txt_nombre_funcionario.getText());
                                func.setApellido_funcionario(this.txt_apellido_funcionario.getText());
                                func.setDireccion_funcionario(this.txt_direccion_fucionario.getText());
                                func.setTelefono_funcionario(this.txt_telefono_funcionario.getText());
                                func.setCargo_funcionario(String.valueOf(this.txt_jComboBox_funcionario.getSelectedItem()));
                                st.update(func);
                                st.getTransaction().commit();
                                this.arranque();
                                JOptionPane.showMessageDialog(null,"Registro actualizado correctamente.");
                                
                                
                            }
                        }
                    }
                }
            }
        }
        
    }
    
    public void obtenerFuncionaro(){
        if(this.jTable5.isEnabled()){
            st.beginTransaction();
            int selectedRow = this.jTable5.getSelectedRow();
            int idFunc = Integer.parseInt(String.valueOf(model.getValueAt(selectedRow, 0)));
            Funcionario func = (Funcionario)st.load(Funcionario.class,idFunc);
            this.txt_id_funcionario.setText(String.valueOf(func.getId_funcionario()));
            this.txt_nombre_funcionario.setText(func.getNombre_funcionario());
            this.txt_apellido_funcionario.setText(func.getApellido_funcionario());
            this.txt_direccion_fucionario.setText(func.getDireccion_funcionario());
            this.txt_telefono_funcionario.setText(func.getTelefono_funcionario());
            this.txt_jComboBox_funcionario.setSelectedItem(func.getCargo_funcionario());
            this.bt_editar.setEnabled(true);
            this.bt_eliminar.setEnabled(true);
                                      
        }
    }
    
