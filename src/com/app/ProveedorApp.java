    public void guardar(){
        // Antes que procedamos a guardar comprobaremos que no hayan campos vacios.
        if(this.txt_descripcion_proveedor.getText().isEmpty()){
            this.txt_descripcion_proveedor.setText("-");
            guardar();
        }else{
            if(this.txt_direccion_proveedor.getText().isEmpty()){
                this.txt_direccion_proveedor.setText("-");
                guardar();
            }
            else{
                if(this.txt_telefono_proveedor.getText().isEmpty()){
                    this.txt_telefono_proveedor.setText("-");
                    guardar();
                }
                else{
                  //Tendremos dos posibles casos nuevo o actualizacion.
                  // Si el boton guardar muestra la etiqueta "Guardar se creara un nuevo requistro.  
                  if(this.bt_guardar.getText().equals("Guardar")){
                      st.beginTransaction();
                     Proveedor prov = new Proveedor();
                     prov.setDes_proveedor(this.txt_descripcion_proveedor.getText());
                     prov.setDireccion_proveedor(this.txt_direccion_proveedor.getText());
                     prov.setTelefono_proveedor(this.txt_telefono_proveedor.getText());
                     st.save(prov);
                     // el comint permite confirmar la transaccion y procesa
                     st.getTransaction().commit();
                     arraque();
                     JOptionPane.showMessageDialog(null,"Proveedor guardado correctamente");
                                           
                  }
                  else{
                   st.beginTransaction();
                   int idProv = Integer.parseInt(this.txt_id_proveedor.getText());
                   //para cargar los registro se utiliza un load con los parametros de id
                   Proveedor prov =(Proveedor)st.load(Proveedor.class, idProv);
                    prov.setDes_proveedor(this.txt_descripcion_proveedor.getText());
                    prov.setDireccion_proveedor(this.txt_direccion_proveedor.getText());
                    prov.setTelefono_proveedor(this.txt_telefono_proveedor.getText());
                    //para el caso sera un update.
                    
                    st.update(prov);
                    st.getTransaction().commit();
                    arraque();
                    JOptionPane.showMessageDialog(null, "El proveedor fue actualizado correctamente.");
                   
                  }
                }
             }
           }
         }
    
