    public void eliminar(){
        //forma de conformar una ele√ßiminacion de registro.
        int showConfirmDialog = JOptionPane.showConfirmDialog(null,"Eliminacion de registro","Desea elimiminar este registro.",JOptionPane.YES_NO_OPTION);
        if(showConfirmDialog == 1){
            JOptionPane.showMessageDialog(null,"Registro Elimindo.");
            
        }
        else{
            // iniciamos una transacion
            st.beginTransaction();
            //Obtenemos la fila selecionado la tabla
            int selectedRow = this.jTable2.getSelectedRow();
            // obtenemos el "id" de la columna "0" de la tabla.
            int idProv = Integer.parseInt(String.valueOf(model.getValueAt(selectedRow, 0)));
            //cargamos el objeto
            Proveedor prov = (Proveedor)st.load(Proveedor.class, idProv);
            //Eliminamos con delete
            st.delete(prov);
            // confirmamos la transacion.
            st.getTransaction().commit();
            JOptionPane.showMessageDialog(null,"Registro Eliminado");
            this.arraque();
            
            
        }
    }
    
   public void informe(){
    //try - para controlar las excepciones.
    try {
        //Creamos una lista de los datos de la table "Tipo" utilizando "List".
        st.beginTransaction();
        List<Proveedor> lista = (List<Proveedor>)st.createQuery("From Proveedor").list();
        //Utilizamos el m√©todo siguiente para cargar el reporte "TipoReport.jasper"
        //El "JRLoader.loadObject" es el cargador.
        JasperReport report  = (JasperReport)JRLoader.loadObject(ClassLoader.getSystemResource("com/informes/reportProveedor.jasper")); 
        //El m√©todo siguiente nos permite pasarle los datos al reporte utilizando JRBeanCollectionDataSource y como argumento la lista que creamos m√°s arriba.
        //La lista posee dos campos por registro: "id" y "des", los nombres se corresponden con las agregadas en el reporte dise√±ado.
        JasperPrint fillReport = JasperFillManager.fillReport(report, null,new JRBeanCollectionDataSource(lista));
        //El JasperViewer para visualizar, le pasamos como argumento nuestro "fillReport" de arriba.
        JasperViewer jviewer = new JasperViewer(fillReport,false);
        //Le damos un t√≠tulo al reporte.
        jviewer.setTitle("Lista de Proveedores.");
        //La hacemos visible.
        jviewer.setVisible(true);
    } catch (HibernateException | JRException e) {
        JOptionPane.showMessageDialog(null, "Error cargando reporte.");
    }
   }
