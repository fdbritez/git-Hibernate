  public Tipo retornoTipo(String desT){
   
      Tipo tipRet = null;
      String desTipo = desT;
      try{
          Query query =st.createQuery("From Tipo t Where t. des_tipo = ?");
          query.setParameter(0,desTipo);
         try{
             tipRet=(Tipo)query.uniqueResult();
         } 
          catch(Exception e){
           JOptionPane.showMessageDialog(null, "Hay más articulos de tipo: " + desTipo); 
      }
  }catch(HibernateException | HeadlessException e){
    
 }
      return tipRet;
 }
  
