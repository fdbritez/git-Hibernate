public void cargarTabla(){
    
    this.limpiartabla();
    List<Articulo> lista = (List<Articulo>)st.createQuery("From Articulo").list();
    for(Articulo articuloList : lista){
        model.addRow(new Object[]{
           articuloList.getId_articulo(),
           articuloList.getDes_articulo(),
           articuloList.getTipo().getDes_tipo(),
           articuloList.getP_venta()
            
        });
    }
}

