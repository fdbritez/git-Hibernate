  public void retornoArticulo(){
      if(this.jTable1.isEnabled()){
          int selectedRow =this.jTable1.getSelectedRow();
          Object valueAt = model.getValueAt(selectedRow, 0);
          int idArt = Integer.parseInt(valueAt.toString());
          Articulo art = (Articulo)st.load(Articulo.class,idArt);
          this.txt_id_articulo.setText(String.valueOf(art.getId_articulo()));
          this.txt_descripcion_articulo.setText(art.getDes_articulo());
          this.combo_tipo.setSelectedItem(art.getTipo().getDes_tipo());
          this.txt_p_compra.setText(String.valueOf(art.getP_compra()));
          this.txt_p_venta.setText(String.valueOf(art.getP_venta()));
          this.txt_porcentaje.setText(String.valueOf(art.getPorcentaje()));
          this.txt_cantidad.setText(String.valueOf(art.getCantidad()));
          this.bt_editar.setEnabled(true);
          this.bt_eliminar.setEnabled(true);
          this.bt_nuevo.setEnabled(false);
      }
  }
  
